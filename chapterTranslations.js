export function getHadithLangCode(language) {
	if (!language) return 'en';
	const lang = language.toLowerCase();
	if (lang.includes('urdu')) return 'ur';
	if (lang.includes('arabic')) return 'ar';
	if (lang.includes('turkish')) return 'tr';
	if (lang.includes('bengali')) return 'bn';
	if (lang.includes('french')) return 'fr';
	if (lang.includes('indonesian')) return 'id';
	if (lang.includes('russian')) return 'ru';
	if (lang.includes('tamil')) return 'ta';
	return 'en';
}

export function getHadithBookName(book, language) {
	const langCode = getHadithLangCode(language);
	const bookTranslations = hadithBookTranslations[book];
	if (!bookTranslations) return '';
	return bookTranslations[langCode] || bookTranslations['en'] || '';
}

export function getHadithChapterName(book, sectionId, language, fallbackName) {
	const bookTranslations = hadithChapterTranslations[book];
	if (!bookTranslations) return fallbackName;
	
	const langTranslations = bookTranslations[language];
	if (!langTranslations) return fallbackName;
	
	return langTranslations[sectionId] || fallbackName;
}

